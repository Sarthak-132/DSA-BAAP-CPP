//1. Arithmatic Operations
// (+, -, *, /, %)
#include <iostream>
using namespace std;

int main() {
  int x = 5;
  int y = 3;
  cout << x + y << endl;    
  cout << x - y << endl;
  cout << y - x << endl;          //-2
  cout << x * y << endl;
  cout << x % y << endl;          //2
  cout << y % x << endl;          //3
  cout << x / y << endl;          //1
//   cout << ++x << endl;
//   cout << --x << endl;
  return 0;
}



